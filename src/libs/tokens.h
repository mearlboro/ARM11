////////////////////////////////////////////////////////////////////////////////

void toks_free(tokens *);

void toks_print(tokens *);

tokens *tokenize(char *, const char *);

void toks_iter(tokens *, toks_func);

char toks_endc(tokens *);

////////////////////////////////////////////////////////////////////////////////

#endif
